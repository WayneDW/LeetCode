class Solution_sort {
public:
    int majorityElement(vector<int>& nums) {
        sort(nums.begin(), nums.end());
        return nums[nums.size() / 2];
    }
};

class Solution_hashtable { // faster
public:
    int majorityElement(vector<int>& nums) {
        unordered_map<int, int> counts; 
        int n = nums.size();
        for (int i = 0; i < n; i++) {
            if (++counts[nums[i]] > n / 2)
                return nums[i];
        }
        return 0;
    }
};


