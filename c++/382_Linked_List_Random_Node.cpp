/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
    ListNode* head;
public:
    /** @param head The linked list's head.
        Note that the head is guaranteed to be not null, so it contains at least one node. */
    Solution(ListNode* head) {
        this->head = head;
    }
    
    /** Returns a random node's value. */
    int getRandom() {
        int i = 2;
        int res = head->val;
        ListNode* node = head->next; // use node we can use function getRandom() repeatedly
        while (node) {
            int j = rand() % i;
            if (j == 0) res = node->val;
            i++;
            node = node->next;
        }
        return res;
    }
};

